// Contoh penggunaan switch case
let bulan = "Oktober";
switch (bulan) {
    case "Oktober":
        document.write("<br>Bulan ini adalah Oktober");
        break;
    case "November":
        document.write("<br>Bulan ini adalah November");
        break;
    default:
        document.write("<br>Bulan ini adalah bulan lain");
}
